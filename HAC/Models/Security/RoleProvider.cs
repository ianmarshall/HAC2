        public override string[] GetRolesForUser(string username)
        {
            List<string> roles = new List<string>();

            if (Configuration.GetConfiguration().AdminEmails.Any(e => e == username)) {
                roles.Add("Admin");
            }
            else
                roles.Add("Visitor");

            return roles.ToArray();
        }


